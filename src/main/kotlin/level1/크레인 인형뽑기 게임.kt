private fun optimizedSolution(board: Array<IntArray>, moves: IntArray): Int {
    var answer = 0
    val basket = Stack<Int>()
    for (move in moves) {
        for (i in board.indices) {
            val item = board[i][move - 1]
            if (item != 0) {
                board[i][move - 1] = 0
                if (basket.isNotEmpty() && basket.peek() == item) {
                    basket.pop()
                    answer += 2
                } else {
                    basket.push(item)
                }
                break
            }
        }
    }
    return answer
}

fun main() {
    val board = arrayOf(
        intArrayOf(0, 0, 0, 0, 0), intArrayOf(0, 0, 1, 0, 3), intArrayOf(0, 2, 5, 0, 1), intArrayOf(4, 2, 4, 4, 2),
        intArrayOf(3, 5, 1, 3, 1)
    )
    val moves = intArrayOf(1, 5, 3, 5, 1, 2, 1, 4)
    val answer = 4

    checkAnswer(solution(board, moves) == answer)
}
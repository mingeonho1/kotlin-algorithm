private fun solution(board: Array<IntArray>, moves: IntArray): Int {
    var answer = 0
    var basket = mutableListOf<Int>()
    val boards = board.toMutableList()
    for (move in moves) {
        for (i in boards.indices) {
            boards[i][move - 1]
            if (boards[i][move - 1] != 0) {
                basket += boards[i][move - 1]
                boards[i][move - 1] = 0
                break
            }
        }
        if (basket.size > 1 && basket[basket.lastIndex] == basket[basket.lastIndex - 1]) {
            answer += 2
            basket.removeAt(basket.lastIndex - 1)
            basket.removeAt(basket.lastIndex)
        }
    }
    return answer
}


private fun solution(number: Int, limit: Int, power: Int): Int {
    var answer = 0
    for (i in 1..number) {
        var divisor = 0
        val sqrt = Math.sqrt(i.toDouble()).toInt()
        for (j in 1..sqrt) {
            if (i % j == 0) {
                divisor += if (i / j == j) 1 else 2
            }
        }
        answer += if (divisor > limit) {
            power
        } else {
            divisor
        }
    }
    return answer
}


private fun solution(n: Int, m: Int, section: IntArray): Int {
    var answer = 0
    var start = section[0]
    for (i in section.indices) {
        if (section[i] - start >= m) {
            answer++
            start = section[i]
        }
    }
    return answer + 1
}


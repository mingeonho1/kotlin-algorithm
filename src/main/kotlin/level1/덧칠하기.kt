private fun solution(n: Int, m: Int, section: IntArray): Int {
    val range = section.last() - section.first() + 1
    return range / m + if (range % m == 0) 0 else 1
}

fun main() {
    val n = 8
    val m = 4
    val section = intArrayOf(2, 3, 6)
    val answer = 2

    checkAnswer(solution(n, m, section) == answer)
}
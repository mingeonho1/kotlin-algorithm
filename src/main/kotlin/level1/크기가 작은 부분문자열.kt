private fun solution(t: String, p: String): Int {
    var answer = 0
    for (i in 0..t.length - p.length) {
        if (p >= t.slice(i until i + p.length)) {
            answer++
        }
    }
    return answer
}

fun main() {
    val t = "10203"
    val p = "15"
    val answer = 3

    checkAnswer(solution(t, p) == answer)
}
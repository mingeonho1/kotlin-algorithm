private fun solution(num: Int): Int {
    var result = num.toLong()
    var index = 0
    while (result != 1L) {
        when {
            result % 2L == 0L -> result /= 2L
            else -> result = result * 3L + 1L
        }
        index++
        if (index == 500) {
            return -1
        }
    }
    return index
}

fun main() {
    val n = 1
    val answer = 0

    checkAnswer(solution(n) == answer)
}
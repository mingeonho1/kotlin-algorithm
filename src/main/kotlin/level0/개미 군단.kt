private fun solution(hp: Int): Int {
    var fullHp = hp
    var answer = 0
    while (fullHp > 0) {
        if (fullHp >= 5) {
            fullHp -= 5
            answer += 1
        } else if (fullHp >= 3) {
            fullHp -= 3
            answer += 1
        } else if (fullHp >= 1) {
            fullHp -= 1
            answer += 1
        }
    }
    return answer
}

fun main() {
    val hp = 999
    val result = 201

    checkAnswer(solution(hp) == result)
}